# Function to print all ANSI codes from the database
print_all_codes() {
  sqlite3 "$DB_FILE" <<EOF
.mode column
.headers on
SELECT 'COLORS' AS category, * FROM COLORS
UNION
SELECT 'EFFECTS' AS category, * FROM EFFECTS
UNION
SELECT 'FORMATS' AS category, * FROM FORMATS;
EOF
}

# Function to get value from the database
get_value() {
  local option="$1"

  sqlite3 "$DB_FILE" <<EOF
SELECT code FROM COLORS WHERE name = '$option'
UNION
SELECT code FROM EFFECTS WHERE name = '$option'
UNION
SELECT code FROM FORMATS WHERE name = '$option'
LIMIT 1;
EOF
}

# Function to get color value
get_color() {
  local color="$1"
  get_value "$color"
}

# Function to get effect value
get_effect() {
  local effect="$1"
  get_value "$effect"
}

# Function to get format value
get_format() {
  local format="$1"
  get_value "$format"
}

